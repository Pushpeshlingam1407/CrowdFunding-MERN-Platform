export const updateProfile = async (req, res) => {
  try {
    const { name, bio, profileImage, companyName, companyWebsite, services } =
      req.body;

    const updateData = {};
    if (name) updateData.name = name;
    if (bio) updateData.bio = bio;
    if (profileImage) updateData.profileImage = profileImage;
    if (companyName) updateData.companyName = companyName;
    if (companyWebsite) updateData.companyWebsite = companyWebsite;
    if (services) updateData.services = services;
    if (req.body.personalPortfolio)
      updateData.personalPortfolio = req.body.personalPortfolio;
    if (req.body.partnerHistory)
      updateData.partnerHistory = req.body.partnerHistory;

    const user = await User.findByIdAndUpdate(req.user.id, updateData, {
      new: true,
      runValidators: true,
    });

    res.status(200).json({
      success: true,
      user,
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};
